%% integrate(F, N) -> fun(A, B) -> float()
%% Возвращает функцию, которая вычисляет приближённый определённый интеграл F от A до B.
%% Используется метод средних прямоугольников (midpoint rule):
%%   - Отрезок [A, B] делится на N равных частей шириной h = (B-A)/N
%%   - На каждой части берётся значение F в середине: F(A + (i-0.5)*h)
%%   - Сумма: h * Σ F(A + (i-0.5)*h), i=1..N
%%
%% Пример:
%%   F1 = integrate(fun(X) -> X end, 100),
%%   F1(0, 1) => ~0.5  (точное значение ∫₀¹ x dx = 0.5)
integrate(F, N) ->
    fun(A, B) ->
        %% Ширина одного прямоугольника
        H = (B - A) / N,
        %% Суммируем по всем N прямоугольникам
        lists:foldl(
            fun(I, Acc) ->
                %% Середина i-го прямоугольника: A + (I - 0.5) * H
                MidX = A + (I - 0.5) * H,
                Acc + F(MidX) * H
            end,
            0.0,
            lists:seq(1, N)
        )
    end.
