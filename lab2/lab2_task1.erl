-export([list_heads/1]).

%% list_heads(List) -> [term()]
%% Возвращает список первых элементов непустых вложенных списков.
%% Все остальные элементы (атомы, кортежи, пустые списки и т.д.) игнорируются.
%%
%% Пример: list_heads([[1,2,3], {true,3}, [4,5], []]) => [1,4]
list_heads([]) ->
    [];
list_heads([[H | _] | Tail]) ->
    %% Текущий элемент — непустой список, берём его голову H
    [H | list_heads(Tail)];
list_heads([_ | Tail]) ->
    %% Текущий элемент — пустой список, кортеж, атом и т.п. — пропускаем
    list_heads(Tail).
