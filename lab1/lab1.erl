-module(lab1).

%% Экспортируем все функции, которые должны быть доступны снаружи модуля
-export([seconds/3, min/1, distinct/1, split_all/2, sublist/3, sublist/2]).


%% ============================================================
%% Задание 1
%% seconds(Hours, Minutes, Seconds) -> integer()
%%
%% Переводит время (часы, минуты, секунды) в количество секунд
%% с начала суток (с 00:00:00).
%% ============================================================
seconds(Hours, Minutes, Seconds) ->
    Hours * 3600 + Minutes * 60 + Seconds.


%% ============================================================
%% Задание 2
%% min(List) -> term()
%%
%% Возвращает минимальный элемент списка.
%% Если список пуст — выбрасывает исключение {error, empty_list}.
%% ============================================================
min([]) ->
    error({error, empty_list});
min([Head | Tail]) ->
    min_helper(Tail, Head).

%% Вспомогательная рекурсивная функция: идёт по хвосту,
%% накапливает текущий минимум в аккумуляторе Acc.
min_helper([], Acc) ->
    Acc;
min_helper([H | T], Acc) when H < Acc ->
    min_helper(T, H);
min_helper([_ | T], Acc) ->
    min_helper(T, Acc).


%% ============================================================
%% Задание 3
%% distinct(List) -> true | false
%%
%% Возвращает true, если все элементы списка уникальны,
%% иначе false.
%% ============================================================
distinct([]) ->
    true;
distinct([Head | Tail]) ->
    case lists:member(Head, Tail) of
        true  -> false;
        false -> distinct(Tail)
    end.


%% ============================================================
%% Задание 4
%% split_all(List, N) -> [[term()]]
%%
%% Разбивает список List на части по N элементов каждая.
%% Последняя часть может быть короче N, если элементов не хватает.
%% ============================================================
split_all([], _N) ->
    [];
split_all(List, N) ->
    %% Берём min(длина_списка, N) чтобы не упасть при lists:split
    %% когда оставшихся элементов меньше, чем N
    TakeCount = erlang:min(length(List), N),
    {Head, Tail} = lists:split(TakeCount, List),
    [Head | split_all(Tail, N)].


%% ============================================================
%% Задание 5
%% sublist(List, N, M)  -> [term()]
%% sublist(List, [N,M]) -> [term()]    (удобный вариант «как в условии»)
%%
%% Возвращает отрезок списка с N-го по M-й элемент включительно
%% (нумерация начинается с 1).
%%
%% Пример из условия: sublist([1,3,4,5,6], [2,4]) => [3,4,5]
%%   позиция 2 -> 3, позиция 3 -> 4, позиция 4 -> 5
%% ============================================================
sublist(List, [N, M]) ->
    sublist(List, N, M).

sublist(List, N, M) ->
    sublist_helper(List, N, M, 1).

%% Вспомогательная функция: I — текущий индекс (начинается с 1).
sublist_helper([], _N, _M, _I) ->
    [];
sublist_helper([H | T], N, M, I) when I >= N, I =< M ->
    %% Текущий элемент входит в нужный диапазон
    [H | sublist_helper(T, N, M, I + 1)];
sublist_helper([_ | T], N, M, I) when I < N ->
    %% Ещё не дошли до начала диапазона — пропускаем
    sublist_helper(T, N, M, I + 1);
sublist_helper(_, _N, _M, _I) ->
    %% Вышли за правую границу — возвращаем пустой список
    [].
